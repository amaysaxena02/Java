public class loopDoWhile {
    public static void main(String[] args) {
        int i = 0;
        do{
            System.out.println("hello\n");
            i++;
        }
        while(i<5);


        int x = 3;
        do{
            System.out.println(x);
            x--;
        }
        while(x>=0);
        System.out.println("\nHappy New Year");
    }
}
