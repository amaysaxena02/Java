public class math {
    public static void main (String[] args)
    { 
        double x = 3.14;
        double y = -10;

        System.out.println(Math.max(x,y));
        System.out.println(Math.min(x,y));
        System.out.println(Math.abs(y));
        System.out.println(Math.sqrt(x));
        System.out.println(Math.round(x));
        System.out.println(Math.ceil(x));  //rounds up to next number always
        System.out.println(Math.floor(x));  //always round down 
    }
}

//these were basic operations from Math class in java